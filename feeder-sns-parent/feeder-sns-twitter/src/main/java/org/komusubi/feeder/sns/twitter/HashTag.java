package org.komusubi.feeder.sns.twitter;

import org.komusubi.feeder.model.Tag;

/**
 * @author jun.ozeki
 */
public class HashTag implements Tag {

    private static final long serialVersionUID = 1L;
    private static final String SYMBOL = "#";
    private String label;
    
    /**
     * create new instance.
     * @param label
     */
    public HashTag(String label) {
        this.label = label;
    }
    
    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((label == null) ? 0 : label.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        HashTag other = (HashTag) obj;
        if (label == null) {
            if (other.label != null)
                return false;
        } else if (!label.equals(other.label))
            return false;
        return true;
    }

    /**
     * @see org.komusubi.feeder.model.Tag#label()
     */
    @Override
    public String label() {
        return SYMBOL + label;
    }

}
